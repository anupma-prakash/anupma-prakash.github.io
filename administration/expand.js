function switch_provost(opt)
{
  if (document.getElementById) {
    if (opt == 'on') {
      document.getElementById("provost").style.display = 'inline';
      document.getElementById("provost_expand").style.display = 'none';
      document.getElementById("provost_collapse").style.display = 'inline';
    }
    if (opt == 'off') {
      document.getElementById("provost").style.display = 'none';
      document.getElementById("provost_expand").style.display = 'inline';
      document.getElementById("provost_collapse").style.display = 'none';
    }
  }
  else {
    reset();
  }
}

